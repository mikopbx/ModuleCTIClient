    /**
     * @param string $russianText
     *
     * @return string
     */
    public static function ruToLat(string $russianText): string
    {
        $transliteratedText = '';

        if ('' !== $russianText) {
            $transliteratedText = \str_replace(
                \array_keys(self::CYRILLIC_TO_ENGLISH_RULES),
                \array_values(self::CYRILLIC_TO_ENGLISH_RULES),
                $russianText
            );
        }

        return $transliteratedText;
    }
}